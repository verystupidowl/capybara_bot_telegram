    public String getInfo(CapybaraInfoDto capybara) {
        StringBuilder sb = new StringBuilder("‚Ñπ –ò–Ω—Ñ–æ—Ä–º–∞—Ü–∏—è –æ —Ç–≤–æ–µ–π –∫–∞–ø–∏–±–∞—Ä–µ:\n\n");

        sb.append("‚ú® –ò–º—è: ").append(capybara.name());

        sb.append("\n ‚òï –ß–∞–µ–ø–∏—Ç–∏–µ ");
        if (Boolean.FALSE.equals(capybara.isTeaWaiting())) {
            sb.append(capybara.canTea()
                    ? "—É–∂–µ –º–æ–∂–Ω–æ"
                    : "—á–µ—Ä–µ–∑: " + capybara.teaTime());
        } else {
            sb.append("–≤ –æ–∂–∏–¥–∞–Ω–∏–∏ —Å–æ–±–µ—Å–µ–¥–Ω–∏–∫–∞");
        }

        if (capybara.hasWork()) {
            if (!capybara.isOnBigJob()) {
                if (!capybara.isWorking()) {
                    sb.append("\nüî® –û—Ç–ø—Ä–∞–≤–∏—Ç—å –Ω–∞ —Ä–∞–±–æ—Ç—É ")
                            .append(capybara.canGoWork()
                                    ? "—É–∂–µ –º–æ–∂–Ω–æ"
                                    : "—á–µ—Ä–µ–∑: " + capybara.workTime());
                } else {
                    sb.append("\nüî® –ó–∞–±—Ä–∞—Ç—å —Å —Ä–∞–±–æ—Ç—ã ")
                            .append(capybara.canTakeFromWork()
                                    ? "—É–∂–µ –º–æ–∂–Ω–æ"
                                    : "—á–µ—Ä–µ–∑: " + capybara.takeFromWork());
                }
            }

            sb.append("\nüíº –ü–æ–≤—ã—à–µ–Ω–∏–µ: ")
                    .append(capybara.rise())
                    .append("/")
                    .append((capybara.index() + 1) * 10);

            if (capybara.isOnBigJob()) {
                sb.append(capybara.canTakeFromBigJob()
                        ? "\nüòè –ú–æ–∂–Ω–æ –∑–∞–±—Ä–∞—Ç—å —Å –±–æ–ª—å—à–æ–≥–æ –¥–µ–ª–∞"
                        : "\nüòè –ú–æ–∂–Ω–æ –∑–∞–±—Ä–∞—Ç—å —Å –±–æ–ª—å—à–æ–≥–æ –¥–µ–ª–∞ —á–µ—Ä–µ–∑: "
                        + timeToString(capybara.takeFromBigJob()));
            } else if (capybara.level() >= 20 && capybara.isWorking()) {
                sb.append(capybara.canGoBigJob()
                        ? "\nüòè –ú–æ–∂–Ω–æ –æ—Ç–ø—Ä–∞–≤–∏—Ç—å –Ω–∞ –±–æ–ª—å—à–æ–µ –¥–µ–ª–æ"
                        : "\nüòè –ú–æ–∂–Ω–æ –æ—Ç–ø—Ä–∞–≤–∏—Ç—å –Ω–∞ –±–æ–ª—å—à–æ–µ –¥–µ–ª–æ —á–µ—Ä–µ–∑: "
                        + timeToString(capybara.bigJobTime()));
            }
        }

        sb.append("\nüåΩ –ü–æ–∫–æ—Ä–º–∏—Ç—å/–æ—Ç–∫–æ—Ä–º–∏—Ç—å ")
                .append(capybara.canSatiety()
                        ? "—É–∂–µ –º–æ–∂–Ω–æ"
                        : "—á–µ—Ä–µ–∑: " + capybara.satietyTime());

        sb.append("\nü•≥ –û—Å—á–∞—Å—Ç–ª–∏–≤–∏—Ç—å ")
                .append(capybara.canHappiness()
                        ? "—É–∂–µ –º–æ–∂–Ω–æ"
                        : "—á–µ—Ä–µ–∑: " + capybara.happinessTime());

        if (!capybara.canRace()) {
            sb.append("\nü©± –í—Ä–µ–º–µ–Ω–∏ –¥–æ –ø–æ–ª–Ω–æ–≥–æ –≤–æ—Å—Å—Ç–∞–Ω–æ–≤–ª–µ–Ω–∏—è –±–æ–¥—Ä–æ—Å—Ç–∏: ")
                    .append(timeToString(capybara.raceTime()));
        }

        sb.append("\n‚¨Ü –£–ª—É—á—à–µ–Ω–∏—è –¥–ª—è –≥–æ–Ω–æ–∫: ").append(capybara.improvement());

        return sb.toString();
    }

